function least(A, B) {
  let sum = 0;
  for (var i = 0; i < B; i++) {
    sum = sum + A[i];
  }

  var char = B - 1;
  let min_sum = sum;
  for (var j = B; j < A.length; j++) {
    sum = sum - A[j - B] + A[j];
    if (sum < min_sum) {
      min_sum = sum;
      char = j;
    }
  }
  return char - B + 1;
}

